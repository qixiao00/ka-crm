CREATE TABLE IF NOT EXISTS weekly_tasks (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  customer_id BIGINT UNSIGNED NOT NULL,
  success_plan_id BIGINT UNSIGNED NULL,
  title VARCHAR(255) NOT NULL,
  owner_manager_id BIGINT UNSIGNED NOT NULL,
  source VARCHAR(80) NOT NULL DEFAULT '主管安排',
  plan_date DATE NOT NULL,
  status ENUM('未开始', '进行中', '逾期', '已完成') NOT NULL DEFAULT '未开始',
  result_note TEXT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_tasks_customer FOREIGN KEY (customer_id) REFERENCES customers(id) ON DELETE CASCADE,
  CONSTRAINT fk_tasks_plan FOREIGN KEY (success_plan_id) REFERENCES success_plans(id) ON DELETE SET NULL,
  CONSTRAINT fk_tasks_owner FOREIGN KEY (owner_manager_id) REFERENCES managers(id),
  INDEX idx_tasks_owner_date (owner_manager_id, plan_date),
  INDEX idx_tasks_status (status)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS push_config (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  webhook_url VARCHAR(500) NULL,
  push_time VARCHAR(16) NOT NULL DEFAULT '18:00',
  timezone VARCHAR(64) NOT NULL DEFAULT 'Asia/Shanghai',
  enabled TINYINT(1) NOT NULL DEFAULT 0,
  include_customer TINYINT(1) NOT NULL DEFAULT 1,
  include_scenario TINYINT(1) NOT NULL DEFAULT 1,
  include_status TINYINT(1) NOT NULL DEFAULT 1,
  include_plan_date TINYINT(1) NOT NULL DEFAULT 1,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS push_recipients (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  config_id BIGINT UNSIGNED NOT NULL,
  manager_id BIGINT UNSIGNED NOT NULL,
  enabled TINYINT(1) NOT NULL DEFAULT 1,
  CONSTRAINT fk_push_recipients_config FOREIGN KEY (config_id) REFERENCES push_config(id) ON DELETE CASCADE,
  CONSTRAINT fk_push_recipients_manager FOREIGN KEY (manager_id) REFERENCES managers(id) ON DELETE CASCADE,
  UNIQUE KEY uniq_push_recipient (config_id, manager_id)
) ENGINE=InnoDB;
