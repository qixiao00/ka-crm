CREATE DATABASE IF NOT EXISTS ka_csm_fujian
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE ka_csm_fujian;

CREATE TABLE IF NOT EXISTS managers (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(64) NOT NULL UNIQUE,
  phone VARCHAR(32) NULL,
  region VARCHAR(64) NOT NULL DEFAULT '福建区',
  enabled TINYINT(1) NOT NULL DEFAULT 1,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS users (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  username VARCHAR(64) NOT NULL UNIQUE,
  display_name VARCHAR(64) NOT NULL,
  password_hash VARCHAR(255) NOT NULL,
  role ENUM('admin', 'supervisor', 'manager') NOT NULL DEFAULT 'manager',
  phone VARCHAR(32) NULL,
  enabled TINYINT(1) NOT NULL DEFAULT 1,
  bound_manager_id BIGINT UNSIGNED NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_users_bound_manager FOREIGN KEY (bound_manager_id) REFERENCES managers(id) ON DELETE SET NULL
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS customers (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(160) NOT NULL UNIQUE,
  parent_customer_id BIGINT UNSIGNED NULL,
  city VARCHAR(64) NOT NULL,
  industry VARCHAR(80) NOT NULL,
  satisfaction_status ENUM('充分信任', '信任支持', '价值无感', '不够满意', '严重不满') NOT NULL DEFAULT '价值无感',
  risk_level ENUM('低', '中', '高') NOT NULL DEFAULT '中',
  manager_id BIGINT UNSIGNED NOT NULL,
  break_amount DECIMAL(12,2) NOT NULL DEFAULT 0,
  renewal_amount DECIMAL(12,2) NOT NULL DEFAULT 0,
  repurchase_amount DECIMAL(12,2) NOT NULL DEFAULT 0,
  eos_amount DECIMAL(12,2) NOT NULL DEFAULT 0,
  remark TEXT NULL,
  last_updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_customers_manager FOREIGN KEY (manager_id) REFERENCES managers(id),
  CONSTRAINT fk_customers_parent FOREIGN KEY (parent_customer_id) REFERENCES customers(id) ON DELETE SET NULL,
  INDEX idx_customers_parent (parent_customer_id),
  INDEX idx_customers_manager (manager_id),
  INDEX idx_customers_status (satisfaction_status),
  INDEX idx_customers_risk (risk_level)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS contacts (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  customer_id BIGINT UNSIGNED NOT NULL,
  name VARCHAR(64) NOT NULL,
  department VARCHAR(120) NULL,
  title VARCHAR(120) NULL,
  level ENUM('高层', '中层', '基层') NOT NULL DEFAULT '基层',
  attitude ENUM('充分信任', '信任支持', '价值无感', '不够满意', '严重不满') NOT NULL DEFAULT '信任支持',
  owner_manager_id BIGINT UNSIGNED NOT NULL,
  last_touch_date DATE NULL,
  notes TEXT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_contacts_customer FOREIGN KEY (customer_id) REFERENCES customers(id) ON DELETE CASCADE,
  CONSTRAINT fk_contacts_owner FOREIGN KEY (owner_manager_id) REFERENCES managers(id),
  INDEX idx_contacts_customer (customer_id),
  INDEX idx_contacts_owner (owner_manager_id)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS success_plans (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  customer_id BIGINT UNSIGNED NOT NULL,
  scenario_name VARCHAR(180) NOT NULL,
  owner_manager_id BIGINT UNSIGNED NOT NULL,
  stage VARCHAR(120) NOT NULL,
  status ENUM('进行中', '已完成', '暂停') NOT NULL DEFAULT '进行中',
  in_kcp TINYINT(1) NOT NULL DEFAULT 0,
  revenue_progress TINYINT UNSIGNED NOT NULL DEFAULT 0,
  relation_progress TINYINT UNSIGNED NOT NULL DEFAULT 0,
  value_progress TINYINT UNSIGNED NOT NULL DEFAULT 0,
  next_action VARCHAR(255) NULL,
  last_updated_at DATE NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_plans_customer FOREIGN KEY (customer_id) REFERENCES customers(id) ON DELETE CASCADE,
  CONSTRAINT fk_plans_owner FOREIGN KEY (owner_manager_id) REFERENCES managers(id),
  INDEX idx_plans_customer (customer_id),
  INDEX idx_plans_owner (owner_manager_id)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS success_plan_contacts (
  success_plan_id BIGINT UNSIGNED NOT NULL,
  contact_id BIGINT UNSIGNED NOT NULL,
  PRIMARY KEY (success_plan_id, contact_id),
  CONSTRAINT fk_plan_contacts_plan FOREIGN KEY (success_plan_id) REFERENCES success_plans(id) ON DELETE CASCADE,
  CONSTRAINT fk_plan_contacts_contact FOREIGN KEY (contact_id) REFERENCES contacts(id) ON DELETE CASCADE
) ENGINE=InnoDB;

